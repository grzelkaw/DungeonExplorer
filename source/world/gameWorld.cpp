void gameWorld::loadNextLevel(int direction) {
	if (direction == 0) {
		currentLevelY++;
		if (currentLevelY >= numCols) {
			currentLevelY = 0;
			currentLevelX++;
			if (currentLevelX >= numRows) {
				currentLevelX = 0;
			}
		}
	}
	else if (direction == 1) {
		currentLevelY--;
		if (currentLevelY < 0) {
			currentLevelY = numCols;
			currentLevelX--;
			if (currentLevelX < 0) {
				currentLevelX = numRows - 1;
			}
		}
	}
	else if (direction == 2) {
		currentLevelX--;
		if (currentLevelX < 0) {
			currentLevelX = numRows;
		}
	}
	else if (direction == 3) {
		currentLevelX++;
		if (currentLevelX >= numRows) {
			currentLevelX = 0;
		}
	}

	int layoutIndex = levelMatrix[currentLevelX][currentLevelY];
	setMapLayout(layoutIndex);
	mapa = layoutIndex;
}

